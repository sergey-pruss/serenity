async function writeSvgWebp(name, svg) {
  const svgPath = path.join(imgDir, `${name}.svg`);
  fs.writeFileSync(svgPath, svg, "utf8");
  const meta = await sharp(Buffer.from(svg)).webp({ quality: 88 }).toBuffer({ resolveWithObject: true });
  await sharp(meta.data).toFile(path.join(imgDir, `${name}.webp`));
  return { w: meta.info.width, h: meta.info.height };
}

