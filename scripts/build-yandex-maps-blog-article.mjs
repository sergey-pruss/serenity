function table(rows, headers) {
  const head = headers
    .map((h) => `<th scope="col">${esc(h)}</th>`)
    .join("");
  const body = rows
    .map(
      (r) => `<tr>${r.map((c) => `<td>${esc(c)}</td>`).join("")}</tr>`
    )
    .join("");
  return `<div class="sa-blog-body-media sa-blog-body-media--bleed"><div class="blog-article-table-scroll"><table class="sa-blog-table-dashed"><thead><tr>${head}</tr></thead><tbody>${body}</tbody></table></div></div>`;
}

